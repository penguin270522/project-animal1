package Animal;

public class Animal {
	public String ten;
	public String loai;
	public String chieuCao;
	public String canNang;
	public String moiTruong;
	public String sinhSan;
	
	public Animal() {
		
	}

	public Animal(String ten, String loai, String chieuCao, String canNang, String moiTruong, String sinhSan) {
		this.ten = ten;
		this.loai = loai;
		this.chieuCao = chieuCao;
		this.canNang = canNang;
		this.moiTruong = moiTruong;
		this.sinhSan = sinhSan;
	}

	
	
	
	
}
